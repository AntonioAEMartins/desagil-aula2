package br.edu.insper.desagil.aula2;

public class Tabuada {

	public String constroi(int n) {
		int indice_primario;
		int indice_secundario;
		String geradora = "";
		// Primeiro Temos que criar as Colunas
		for (n = n ; indice_secundario <= n; indice_secundario++) {
			for (indice_primario = 0; indice_primario <= n; indice_primario++) {
				
				return geradora;
			}
		}
		
		// Depois temos que crias as linhas
		return "";
		
	}

}
